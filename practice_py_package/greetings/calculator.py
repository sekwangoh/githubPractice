# add함수는 num1와 num2를 더한 결과값을 알려주는 함수이다.
def add(num1, num2):
    return num1 + num2

# subtract함수는 num1와 num2를 뺀 결과값을 알려주는 함수이다.


def subtract(num1, num2):
    return num1 - num2

# multiplication함수는 num1와 num2를 곱한 결과값을 알려주는 함수이다.


def multiplication(num1, num2):
    return num1 * num2

# division함수는 num1와 num2를 나눈 결과값을 알려주는 함수이다.


def division(num1, num2):
    return num1 / num2
